DROP DATABASE IF EXISTS HerpDB;
CREATE DATABASE HerpDB;

CREATE TABLE classRoster (
		memberNo	int(4)			NOT NULL,
		classNo		varchar(10),
		className	varchar(50),
		memberName	varchar(60),
		PRIMARY KEY(memberNo)
	);
	
CREATE TABLE speciesNorthAlabama (
		speciesNo   	int(4)			NOT NULL,
		speciesClass	varchar(60),
		speciesOrder	varchar(60),
		speciesFamily	varchar(60),
		speciesGenus	varchar(60),
		speciesSpecies	varchar(60),
		commonName		varchar(60),
		PRIMARY KEY (speciesNo)
	);
	
CREATE TABLE locationData (
		instanceNo			int(8)				NOT NULL,
		fieldDate			varchar(10),
		GeneralLocation		varchar(50),
		speciesNo			int(4),
		Temperature			varchar(5),
		Easting				decimal(9,2),
		Northing			decimal(9,2),
		locationTime		varchar(8),
		Microhabitat 		varchar(100),
		Behavior			varchar(8),
		FoundBy				int(4),
		PRIMARY KEY(instanceNo),
		CONSTRAINT fk_FoundBy FOREIGN KEY(Foundby) REFERENCES classRoster(memberNo),
		CONSTRAINT fk_speciesNo FOREIGN KEY(speciesNo) REFERENCES speciesNorthAlabama(speciesNo)
	);

CREATE TABLE images (
		imageNo 		int(4)			NOT NULL,
		instanceNo 		int(8),
		image1			varchar(80),
		image2			varchar(80),
		image3			varchar(80),
		image4			varchar(80),
		image5			varchar(80),	
		PRIMARY KEY(imageNo),
		CONSTRAINT fk_instanceNo FOREIGN KEY(instanceNo) REFERENCES locationData(instanceNo)
	);
	
